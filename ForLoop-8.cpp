#include <bits/stdc++.h>
using namespace std;

int main()
{
    int i;
    for (i = 1; i <= 5; i++)
    {
        cout << "sandeep " << i << "\n";
    }
    // truncated 'i' when condition in the not satisfied
    cout << i << "\n";

    // for loop in the reverse order
    int j;
    for (j = 5; j > 0; j--)
    {
        cout << "sandeep " << j << "\n";
    }
    cout << j;
    return 0;
}