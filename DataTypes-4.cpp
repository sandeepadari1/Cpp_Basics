#include <bits/stdc++.h>
using namespace std;

int main()
{
    // int , long , long long (for high range) - Dtype
    int x = 5;
    cout << x << "\n";

    long y = 100;
    cout << y << "\n";

    long long z = 150000000;
    cout << z << "\n";

    // float , double - Dtype
    float f = 5.6;
    double d = 5.67411;
    cout << f << " " << d << "\n";

    // string - Dtype
    string str;
    getline(cin, str);
    cout << str << "\n";

    // char - Dtype
    char ch = 's';
    cout << ch;
}